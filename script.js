  const parseTextToTiles = (text) => {
    builtWord = [];
    let i = 0;
    text = text.toLowerCase().replace(/[^\u1000-\u109Fa-z]/g, '');

    const currentData = kanaData[currentScript];
    const allEntries = [
      ...currentData.main,
      ...currentData.dakuten,
      ...currentData.combo
    ].filter(item => item[0] !== "" && item[1] !== "");

    const isKata = currentScript === "katakana";

    while (i < text.length) {
      let matched = false;
      
      // 1. Check for Sokuon (double consonants like 'tt', 'kk') - only for Romaji letters
      if (i + 1 < text.length && text[i] === text[i+1] && /[a-z]/.test(text[i]) && !"aeiou".includes(text[i]) && text[i] !== 'n') {
        const smallTsu = isKata ? 'ッ' : 'っ';
        builtWord.push({ symbol: smallTsu, romaji: text[i], burmese: "်" });
        i++;
        continue;
      }

      // 2. Match longest combinations (up to 6 chars for Burmese, 3 for Romaji)
      for (let len = 6; len >= 1; len--) {
        if (i + len <= text.length) {
          const chunk = text.substring(i, i + len);
          
          if (chunk === 'n' && len === 1) {
             if (i + 1 < text.length && "aeiouy".includes(text[i+1])) {
                 break;
             }
             const nEntry = allEntries.find(e => e[1] === 'n');
             if (nEntry) {
                 builtWord.push({ symbol: nEntry[0], romaji: nEntry[1], burmese: nEntry[2] });
                 matched = true;
                 i += len;
                 break;
             }
          }

          const match = allEntries.find(e => e[1] === chunk || e[2] === chunk);
          if (match) {
            // Check for Chouon specifically for Katakana consecutive vowels
            if (isKata && chunk.length === 1 && "aeiou".includes(chunk)) {
               if (builtWord.length > 0) {
                 const prevVowel = builtWord[builtWord.length - 1].romaji.slice(-1);
                 if (chunk === prevVowel) {
                   builtWord.push({ symbol: 'ー', romaji: chunk, burmese: match[2] });
                   matched = true;
                   i += len;
                   break;
                 }
               }
            }
            
            builtWord.push({ symbol: match[0], romaji: match[1], burmese: match[2] });
            matched = true;
            i += len;
            break;
          }
        }
      }

      if (!matched) {
        i++;
      }
    }
    updateBuilderUI();
  };

